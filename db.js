// ── Shot document ────────────────────────────────────────────────────────────

/**
 * Save a single shot to Firestore (Task 5 format).
 *
 * @param {{
 *   userId:          string,
 *   sessionId:       string,
 *   ai_top:          string,     // 'Make' | 'Miss' | 'Not-a-shot'
 *   ai_subtype:      string|null,
 *   confidence:      number,
 *   user_top:        string,
 *   user_subtype:    string|null,
 *   comment:         string,
 *   source:          string,     // 'auto' | 'manual'
 *   event_type:      string,     // 'basket' | 'impact'
 *   device_event_ts: number,     // device-side event time (s)
 *   host_event_ts:   number,     // host browser time of event (s)
 *   video_clip_ts:   number,     // video time of event (s)
 *   video_clip_url:  string|null,
 * }} shot
 */
export async function saveShot(shot) {
  const shotsRef = collection(db, 'shots');
  const docRef   = await addDoc(shotsRef, {
    ...shot,
    createdAt: Timestamp.now(),
  });
  return docRef.id;
}

// ── Session document ─────────────────────────────────────────────────────────

/**
 * Save a practice session summary (Task 3 format).
 * The caller builds the full document; this function just writes it.
 *
 * @param {string} uid
 * @param {string} sessionId
 * @param {object} sessionDoc  Full session document matching the Task 3 schema
 */
export async function saveSession(uid, sessionId, sessionDoc) {
  const sessionRef = doc(db, 'users', uid, 'sessions', sessionId);
  await setDoc(sessionRef, sessionDoc);
}

// ── Queries ──────────────────────────────────────────────────────────────────

/**
 * Fetch recent sessions for a user, newest first.
 * @param {string} uid
 * @param {number} [maxSessions=20]
 * @returns {Promise<Array>}
 */
export async function fetchSessions(uid, maxSessions = 20) {
  const sessRef = collection(db, 'users', uid, 'sessions');
  const q       = query(sessRef, orderBy('createdAt', 'desc'), limit(maxSessions));
  const snap    = await getDocs(q);
  return snap.docs.map(d => ({ id: d.id, ...d.data() }));
}

/**
 * Fetch shots for a specific session.
 * @param {string} uid
 * @param {string} sessionId
 * @returns {Promise<Array>}
 */
export async function fetchSessionShots(uid, sessionId) {
  const q    = query(
    collection(db, 'shots'),
    where('userId',    '==', uid),
    where('sessionId', '==', sessionId),
    orderBy('createdAt', 'asc'),
  );
  const snap = await getDocs(q);
  return snap.docs.map(d => ({ id: d.id, ...d.data() }));
}

/**
 * Fetch all shots for lifetime stats.
 * @param {string} uid
 * @returns {Promise<Array>}
 */
export async function fetchAllShots(uid) {
  const q    = query(
    collection(db, 'shots'),
    where('userId', '==', uid),
    orderBy('createdAt', 'asc'),
  );
  const snap = await getDocs(q);
  return snap.docs.map(d => ({ id: d.id, ...d.data() }));
}
